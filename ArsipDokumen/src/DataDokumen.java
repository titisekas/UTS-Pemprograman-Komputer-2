    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int r = tblDokumen.getSelectedRow();
        if (r !=-1){ //maka sudah ada baris yg dipilih
            int kd = Integer.parseInt(tblDokumen.getValueAt(r, 1).toString());
            int pilih = JOptionPane.showConfirmDialog(this,"" //kolom ke 1 adalah kolom yang tadi di hidden
                    +"Apakah anda yakin?","Konfirmasi",
                    JOptionPane.YES_NO_OPTION); //dialog konfirmasi yes dan no
            if (pilih == 0) { 
                try {
                    java.sql.Connection c = Connection.MySQL(); //mendeteksi jika ada yang error
                    Statement st = c.createStatement();
                    String q = "DELETE FROM dokumen WHERE kode="+kd+"";
                    st.executeUpdate(q);
                    loadData("");
                    JOptionPane.showMessageDialog(this, "Data telah dihapus");
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

