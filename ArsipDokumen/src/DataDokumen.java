    private void loadData(String key) {
        try {
            model.setRowCount(0);
            java.sql.Connection c = Connection.MySQL();
            Statement st = c.createStatement();
            String where = key.isEmpty()?"": " WHERE nama LIKE '%"+key+"%'";
            String q = "";
            ResultSet rs = st.executeQuery("SELECT * FROM dokumen"+where);
            int no = 0;
            while (rs.next()) {
                no++;
                int kd = rs.getInt("kode");
                String nama = rs.getString("nama");
                String kategori = rs.getString("kategori");
                String lokasi =rs.getString("lokasi");
                String deskripsi = rs.getString("deskripsi");
                String tanggal =rs.getString("tanggal");
                Object[] data = {no, kd, nama,kategori,lokasi,deskripsi,tanggal};
                model.addRow(data);
            }
        } catch (Exception e) {
        }
    }

